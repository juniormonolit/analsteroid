-- Роль «Логист» + сужение «Пользователь» до заглушки + перевод существующих
-- логистов (распоряжение владельца, 10.07.2026). БД: YC system.
-- НЕ применять локально — накатывает Артём на проде атомарно.
--
-- 1) Новая системная роль «Логист»: Продажи + Реализация. Ключ каталога
--    section.realization заведён в lib/auth/perms.ts (PERM_SECTIONS) —
--    сам раздел «Реализация» в сайдбаре пока задизейблен («скоро», без
--    href), право выдаётся впрок, на будущее включение раздела. ЛК
--    (/profile) правом не гейтится — доступен всем залогиненным как есть.
--
-- 2) Роль «Пользователь» становится безопасной заглушкой: permissions → '{}'.
--    Расчёт: это дефолт для новых аккаунтов, которым авто-подстановка роли
--    по логину (см. lib/auth/roleByLogin.ts) не присвоила ничего конкретного
--    (МОП/РОП/Логист) — то есть «если автоматика не сработала, пользователь
--    не увидит ничего лишнего». ЛК не гейтится — остаётся доступен всем.
--
-- 3) Перевод logist2202 и logist2215 → «Логист»: после сужения «Пользователь»
--    до '{}' они остались бы с голым ЛК без Продаж — переносим явно, чтобы
--    не потерять доступ. Тестовые test_alfred_* НЕ трогаем.
--
-- Идемпотентно: INSERT роли — ON CONFLICT DO NOTHING; UPDATE'ы безусловны
-- (повторный запуск даёт тот же результат — безопасно перезапускать).

INSERT INTO roles (name, description, is_system, permissions) VALUES
('Логист', 'Логист — видит Продажи и Реализацию (раздел «скоро», право впрок)', true,
 ARRAY['section.sales', 'section.realization'])
ON CONFLICT (name) DO NOTHING;

UPDATE roles SET permissions = '{}'
WHERE name = 'Пользователь';

UPDATE users SET role_id = (SELECT id FROM roles WHERE name = 'Логист')
WHERE login IN ('logist2202', 'logist2215');
