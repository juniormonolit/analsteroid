-- Права v2: персональные исключения видимости разделов ("доп. галки лично
-- юзеру", союз с правами роли). Архитектурное решение (не join-таблица):
-- text[]-колонка на users, тем же паттерном, что и roles.permissions —
-- переиспользуем sanitizePermissions()-подобную валидацию (см.
-- sanitizeSectionOverrides в lib/auth/perms.ts), не требует join'а при каждом
-- чтении сессии (lib/auth/session.ts UNION'ит роль+оверрайды одним запросом,
-- как и раньше — один SELECT на реквест).
--
-- Ограничение по каталогу: сюда можно писать только section.* ключи (не
-- action.*) — персональные исключения только про видимость разделов, права на
-- действия (редактирование планов, управление пользователями и т.п.) остаются
-- только через роль. Валидируется в коде (app/api/admin/users/[id]/overrides),
-- не CHECK-констрейнтом — каталог прав живёт в lib/auth/perms.ts, не в БД
-- (тот же принцип, что и у roles.permissions).
--
-- Выдавать могут и админы (action.users.manage), и супер-админ — см. отчёт.
--
-- Идемпотентно, ADD COLUMN IF NOT EXISTS — существующих юзеров не трогает
-- (дефолт '{}' == пусто, поведение не меняется до первого явного назначения).
-- БД: YC system. НЕ применять локально — накатывает Артём на проде атомарно.

ALTER TABLE users ADD COLUMN IF NOT EXISTS section_overrides text[] NOT NULL DEFAULT '{}';
