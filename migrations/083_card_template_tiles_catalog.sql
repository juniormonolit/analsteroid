-- Шаблоны карточек v3 (задача 10.07, пакет «карточка v4», п.1: «плитки итогов из
-- ВСЕХ метрик каталога, не 6 зашитых чекбоксов»). Расширяет card_templates
-- (миграция 073, СХЕМА уже накатана и содержит 2 строки — 'manager'/'department';
-- миграция 075 перевела ТОЛЬКО колонку axes в {metricKey,invert}[] — tiles тогда
-- не трогалась и осталась ПЛОСКИМ массивом camelCase-строк, напр.
-- ["reservations","confirmedReservations","salesCount","salesAmount","shipments",
-- "avgCheck"], проверено живым запросом 10.07) — колонка tiles остаётся JSONB
-- (DDL не меняется), меняется только ФОРМА ДАННЫХ внутри: голые camelCase-строки →
-- те же строки с префиксом «legacy:».
--
-- Зачем: раньше 6 плиток были зашитым enum'ом (lib/settings/cardTemplates.ts::
-- TILE_CATALOG_KEYS), чекбокс «показывать/нет» на каждой, порядок не настраивался.
-- Теперь плитка — ЛЮБАЯ метрика полного каталога (lib/metrics/catalog.ts::
-- loadMetrics(), ~195 видимых), выбор count НЕ ограничен (в отличие от MAX_AXES=6
-- у осей паутины), порядок = порядок элементов в массиве (тот же приём, что и оси).
-- В отличие от axes, tiles НЕ становится массивом объектов — плиткам не нужен
-- invert (не участвуют в скоринге/перцентиле, только значение + Δ% к периоду
-- сравнения), поэтому формат хранения — ПРОСТОЙ массив строк с префиксом «legacy:»
-- у исходных 6 (см. LEGACY_TILE_KEYS в lib/settings/cardTemplates.ts) либо голый id
-- каталога у произвольной метрики.
--
-- Префикс «legacy:» — по аналогии с осями (075): защита на случай, если когда-нибудь
-- в каталог метрик добавят id, буквально совпадающий с одним из camelCase-имён
-- (маловероятно — id каталога snake_case, а не camelCase, но единообразие с осями
-- и одна и та же кодовая база парсинга дешевле отдельного пути для tiles).
--
-- ИНВАРИАНТ (как и в 073/075): после наката НИ ОДНА цифра/видимость плиток НЕ
-- меняется, пока админ не тронет форму — префикс не меняет состав/порядок выбранных
-- плиток, только форму хранения ключа.
--
-- Идемпотентна: WHERE-условие бьёт только строки, где tiles[0] — ЕЩЁ голая строка
-- БЕЗ префикса «legacy:» (старый формат); после первого наката tiles[0] уже
-- начинается с «legacy:», повторный запуск миграции — no-op (не перезатирает уже
-- настроенный админом список/порядок плиток).
--
-- БД: YC system. НЕ применять локально — накатывает Артём на проде атомарно.
-- Проверено 10.07 (Николай): 083 — первый свободный номер (локально migrations/ до
-- 082 включительно заняты параллельной задачей — 078 будет переименована в 082 при
-- батч-деплое Артёма; на сервере ad-hoc номера заняты по 081) — 083 выбран заведомо
-- выше обоих диапазонов, повторно проверить свободность на стороне Артёма перед
-- накатом. Dry-run (BEGIN...ROLLBACK) на живой system БД 10.07: обе строки
-- ('manager'/'department') корректно переведены в ["legacy:reservations", ...],
-- повторный прогон на уже мигрированных данных — 0 строк задето (идемпотентность
-- подтверждена).

UPDATE card_templates
SET tiles = (
  SELECT jsonb_agg('legacy:' || (elem #>> '{}'))
  FROM jsonb_array_elements(tiles) AS elem
),
updated_at = NOW()
WHERE jsonb_typeof(tiles) = 'array'
  AND jsonb_array_length(tiles) > 0
  AND jsonb_typeof(tiles -> 0) = 'string'
  AND (tiles -> 0 #>> '{}') NOT LIKE 'legacy:%';
