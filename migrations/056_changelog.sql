-- Фича «Что изменилось?» — ченджлог для конечных пользователей приложения (макет
-- согласован владельцем: changelog-notifications-mock.html). Лента записей общая для
-- всех, read-state — НА УРОВНЕ АККАУНТА (users.changelog_seen_at), не браузера,
-- потому что таблица users (как и session/auth) живёт в этой же БД (см.
-- lib/db/clients.ts: systemDb() = YC system, там же saved_reports.shared_section,
-- users.ui_mode и т.п. per-user серверные настройки — тот же паттерн).
-- Непрочитанные для юзера = COUNT(changelog_entries WHERE published_at >
-- COALESCE(users.changelog_seen_at, '-infinity')). «Отметить прочитанным» = UPDATE
-- users SET changelog_seen_at = now().
-- БД: YC system (run_system.mjs).

CREATE TABLE IF NOT EXISTS changelog_entries (
  id           uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  published_at timestamptz NOT NULL,
  category     text NOT NULL,
  title        text NOT NULL,
  body         text NOT NULL,
  created_at   timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS changelog_entries_published_at_idx
  ON changelog_entries (published_at DESC);

-- Ключ идемпотентности сида ниже — (title, published_at) не должны повторяться.
CREATE UNIQUE INDEX IF NOT EXISTS changelog_entries_title_published_uniq
  ON changelog_entries (title, published_at);

ALTER TABLE users ADD COLUMN IF NOT EXISTS changelog_seen_at timestamptz;

