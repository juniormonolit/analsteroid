-- ПРЕДЛОЖЕНИЕ (НЕ ПРИМЕНЯТЬ без решения владельца) — задача 10.07, фикс «план-метрики
-- должны считать рабочие дни ПО ВЫБРАННОМУ ПЕРИОДУ, а не по "сегодня"».
--
-- Контекст: 'plan_sales_today'/'plan_shipments_today' называются «План продаж (на
-- сегодня)» / «План отгрузок (на сегодня)» с миграции 018 (название не менялось в 041).
-- До этого фикса они реально считали план "от начала месяца по сегодня" — название было
-- честным. После фикса (app/api/reports/run/route.ts, computePeriodPlanByLogin) значение
-- считается по ВЫБРАННОМУ ПЕРИОДУ отчёта (рабочие дни в пересечении периода и "сегодня"),
-- т.е. "на сегодня" в названии стало неточным: при периоде 01-05.07 метрика покажет план
-- за 01-05.07, а не "план на сегодняшний день".
--
-- Это ПРЕДЛОЖЕНИЕ переименовать в "(на период)" — файл подготовлен, но НЕ применён к
-- БД (ни YC analytics, ни прод). Применять только после явного решения владельца — есть
-- риск, что название "(на сегодня)" уже привычно пользователям и смена может запутать
-- сильнее, чем помочь. Альтернатива — оставить старое название "(на сегодня)" как есть
-- (решение по умолчанию, если владелец не даст добро на ренейм).
--
-- БД: YC analytics (таблица metrics), run_analytics.mjs.

UPDATE metrics SET
  name_ru = 'План продаж (на период)',
  name_short_ru = 'План прод. (период)'
WHERE id = 'plan_sales_today';

UPDATE metrics SET
  name_ru = 'План отгрузок (на период)',
  name_short_ru = 'План отгр. (период)'
WHERE id = 'plan_shipments_today';
