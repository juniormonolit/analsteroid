// Отображение филиала (п.1 правок 09.07/2 — «Филиалы — display-слой»): ТОЛЬКО
// оформление вывода — карточка сделки, группировка «По филиалу», фильтры/дропдауны.
// Ключи в БД (org_resolved_hierarchy.branch) и любые сравнения по ним (branchName,
// dimensionId групп и т.п.) НЕ меняются — функция применяется исключительно в
// точке вывода строки на экран, сырое значение остаётся прежним и продолжает
// использоваться для маршрутизации дрилл-дауна/фильтров.
//
// НЕ ПУТАТЬ с BRANCH_LABELS (lib/org/deptCategories.ts) — та мапа переводит те же
// сырые значения в короткие коды (СПБ/МСК/КРД) для резолва КАТЕГОРИИ отдела внутри
// филиала, у неё другой потребитель (resolveDeptCategory) и её трогать нельзя.
//
// Маркетинговое гео-измерение «Филиал» (marketing_sources.branch, дрилл-даун по
// источникам) — отдельная колонка БД с другим смыслом, эта функция там НЕ применяется.
const DISPLAY_LABELS: Record<string, string> = {
  'СПб': 'Санкт-Петербург',
  'Москва/МО': 'Москва',
  // «Краснодар» и любые прочие значения — без записи в мапе, возвращаются как есть.
};

export function branchLabel(raw: string | null | undefined): string {
  if (!raw) return raw ?? '';
  return DISPLAY_LABELS[raw] ?? raw;
}
